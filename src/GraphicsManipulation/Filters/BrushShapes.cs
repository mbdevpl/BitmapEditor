
namespace GraphicsManipulation.Filters
{
	/// <summary>
	/// Enumerates available shapes of brushes.
	/// </summary>
	public enum BrushShapes
	{
		/// <summary>
		/// Brush applies to the whole bitmap.
		/// </summary>
		Fill,
		/// <summary>
		/// Brush is square-shaped.
		/// </summary>
		Square,
		/// <summary>
		/// Brush has shape of a circle.
		/// </summary>
		Circle
	}
}
